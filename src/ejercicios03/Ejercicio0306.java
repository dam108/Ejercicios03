package ejercicios03;
public class Ejercicio0306 {
    public static void main(String[] args) {
        //mostrar numeros pares comprendidos entre el 100 y el 1 en orden descendente
        int i;
        for (i = 100; i >= 1; i-= 2){ // empezamos en 100 mientras 100 sea mayor o igual que 1 restale 2 a i
            System.out.println(i);
        }
    }
}